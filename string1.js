// Using Single Quote
let s1 = 'abcd';
console.log(s1);

// Using Double Quote
let s2 = "abcd";
console.log(s2);

//create using constructor
let s = new String('abcd');
console.log(s);

//string interpolation
let s3 = 'gfg';
let s4 = `You are learning from ${s3}`;

console.log(s4);

//empty string
let s5 = '';
let s6 = "";

console.log(s5);
console.log(s6);

//multiline string
let s7 = `
    This is a
    multiline
    string`;

console.log(s7);